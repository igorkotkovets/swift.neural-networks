    func testReadingBigCVSFile() {
        let filePath = bundle.path(forResource: "mnist_train_100", ofType: "csv")
        XCTAssertNotNil(filePath)
        let fileReader = try? FileReader(filePath: filePath!)
        XCTAssertNotNil(fileReader)

        var line: String?
        var readCount = 0
        var parseCount = 0
        repeat {
            line = fileReader?.readLine()
            if line != nil {
                readCount += 1
                let bitmap = parser.parse(line!)
                if bitmap != nil {
                    parseCount += 1
                }
            }
        } while line != nil

        XCTAssertEqual(100, readCount)
        XCTAssertEqual(100, parseCount)
    }

    func testParsingSeveralValuesFromBigFile() {
        let filePath = bundle.path(forResource: "mnist_train_100", ofType: "csv")
        XCTAssertNotNil(filePath)
        let fileReader = try? FileReader(filePath: filePath!)
        XCTAssertNotNil(fileReader)

        var line = fileReader?.readLine()
        XCTAssertNotNil(line)
        let parser = CharactersParser()
        var bitmap = parser.parse(line!)
        XCTAssertNotNil(bitmap)
        XCTAssertEqual(5, bitmap!.value)

        line = fileReader?.readLine()
        XCTAssertNotNil(line)
        bitmap = parser.parse(line!)
        XCTAssertNotNil(bitmap)
        XCTAssertEqual(0, bitmap!.value)
    }

    

